        <p>
            This temple complex was a brainchild of Sri Yamavaram Chandrashekhara Sharma scholar follower of goddesses
            Saraswathi astrologer and a Vastu expert. Being a businessman he has multi-faceted activities to his credit.
        </p>
        <p>
            In 1998 the building process for the above temple was activated under the group who called themselves, Satya
            Patam Seva Samethi. This committee has started searching for a convenient place to build a temple.
            Ultimately they selected the hillock of Vargal, where 400 years old Shambu Deva Temple was situated. This
            temple is 2 feet below the ground level and one has to pass by crawl on the ground for a few feet to reach
            the main Shiva lingam. Surrounding this temple were two ancient Vaishnaya Temples which were built during or
            before the Kakatiya rulers. There is a big Victory Pillar made of rock which is around 30 feet in height.
            The Victory Pillar has the statues of SitaRama Lakshmana, Goddesses Lakshmi, and entwined a couple of
            snakes.
        </p>
        <p>
            On Vasantha Panchami of 1989, the Bhoomi pooja was performed and the foundation stone was laid for the
            temple of Sri Saraswathi Devi. It should be noted on this day they had only Rs. 2700 only.When they started
            propagating about the process of building Saraswathi Temple of donations poured in like a flood. In this
            way, the construction process continued without any hindrance with the help of Lord Saraswathi.
        </p>
        <p>
            In 1992 Magashudda Trayodashi Pushpagiri petadipate Sri Sri Sri Vidya Nrusinha Bharati Swamy has laid the
            foundation for the statues of Goddesses Sri Vidya Saraswathi Devi and Lord Shani statues in a temple. This
            temple was later dedicated to Kanchi Petam and a Vedic Patashala was started in 1999 by Sri Shankara Vijaya
            Saraswathi of Kanchi Petam and Lakshmi Ganapathi was inaugurated in 2001. The Expenditure crossed 1 crore of
            the rupees for building this temple. The temple has 13 and a half acres of land on the east side where a
            park, library, hospital was planned in their future expansion.
        </p>
        <p>
            Every day 100 around people were given free food to come to visit the temple at a big dining hall in the
            temple complex. During the Dussehra period 1000’s of devotees visit the temple every day. The Dussehra
            temple starts with Vigneshwara Pooja. Maha Abishekam Navaratri Kalasha Sthapana Chathu Sha shatyu pachara
            pooja, Arathi, Mantha Pushpam, Kukumarchana were performed. Laksha pooja Archana Pustaka Roopini Saraswathi
            Pooja Ashtothara Shathakalasha Abishekam On the last day of Vijayadarshanam of the goddesses Saraswati is
            decorated for devotees.
        </p>
        <p>
            Pooja for Lord Shani Special pooja will be performed on every Shani Trayodashi which comes on the Saturday.
            The poojas for Lord Shani started at 5 a.m. in the morning which starts with Ganapathi pooja and Japam,
            Homam will be performed with Tarpana to Lord Shani. The Lord Sheni will be worshipped with the oil called
            Taila Abishekam. The Homam for Lord Sheni will be ended at 12:00 noon with Poornahuthi, Arathi, etc., This
            is a very big event for this temple which attracts 1000’s of devotees from all over Telangana, A.P and other
            places of India.
