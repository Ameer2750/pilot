                <p>According to some Hindu texts such as the Shakta and Vaishnava Puranas, Navaratri theoretically falls
                    twice or four times a year. Of these, the Sharada Navaratri near autumn equinox (September-October)
                    is the most celebrated and the Vasanta Navaratri near spring equinox (March-April) is next most
                    significant to the culture of Indian subcontinent. In all cases, Navaratri falls in the bright half
                    of the Hindu luni-solar months. The celebrations vary by region, leaving much to the creativity and
                    preferences of the Hindu.
                </p>
                <p>
                    <strong>Sharada Navaratri:</strong>
                    the most celebrated of the four navaratris, named after sharada which means autumn. It is observed
                    the lunar month of Ashvin (post-monsoon, September–October). In many regions the festival falls
                    after autumn harvest, and in others during harvest.
                </p>
                <p>
                    <strong>Vasanta Navaratri:</strong>
                    the second most celebrated, named after vasanta which means spring. It is observed the lunar month
                    of Chaitra (post-winter, March–April). In many regions the festival falls after spring harvest, and
                    in others during harvest.
                </p>
                <p>
                    The other two navratris are observed regionally or by individuals:
                </p>
                <p>
                    <strong>Magha Navaratri:</strong>
                    in Magha (January–February), winter season. The fifth day of this festival is often independently
                    observed as Vasant Panchami or Basant Panchami, the official start of spring in the Hindu tradition
                    wherein goddess Saraswati is revered through arts, music, writing, kite flying. In some regions, the
                    Hindu god of love, Kama is revered.
                </p>
                <p>
                    <strong>Ashada Navaratri:</strong>
                    &nbsp;in Ashadha (June–July), start of the monsoon season. The Sharada Navaratri commences on the
                    first day (pratipada) of the bright fortnight of the lunar month of Ashvini. The festival is
                    celebrated for nine nights once every year during this month, which typically falls in the Gregorian
                    months of September and October. The exact dates of the festival are determined according to the
                    Hindu luni-solar calendar, and sometimes the festival may be held for a day more or a day less
                    depending on the adjustments for sun and moon movements and the leap year.
                </p>
                <p>
                    The festivities extend beyond goddess Durga and god Rama. Various other goddesses such as Saraswati
                    and Lakshmi, gods such as Ganesha, Kartikeya, Shiva and Krishna are regionally revered. For example,
                    a notable pan-Hindu tradition during Navaratri is the adoration of Saraswati, the Hindu goddess of
                    knowledge, learning, music and arts through Ayudha Puja. On this day, which typically falls on the
                    ninth day of Navaratri after the Good has won over Evil through Durga or Rama, peace and knowledge
                    is celebrated. Warriors thank, decorate and worship their weapons, offering prayers to Saraswati.
                    Musicians upkeep their musical instruments, play and pray to them. Farmers, carpenters, smiths,
                    pottery makers, shopkeepers and all sorts of trades people similarly decorate and worship their
                    equipment, machinery and tools of trade. Students visit their teachers, express respect and seek
                    their blessings. This tradition is particularly strong in South India, but is observed elsewhere
                    too.
                </p>
            </div>

