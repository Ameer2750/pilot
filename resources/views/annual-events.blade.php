                </p>
                <p>
                    Vasant Panchami is a festival that marks the arrival of spring. It is celebrated by people in
                    various ways depending on the region.Vasant Panchami also marks the start of preparation for holiday
                    and holi which occurs forty days later.For many Hindus, Vasant Panchami is the festival dedicated to
                    goddess Saraswati who is their ancient goddess of knowledge, language, music and all arts. She is
                    the energy of Brahma, and she symbolizes creative energy and power in all its form, including
                    longing and love (kama). The season and festival also reflects the agricultural fields which are
                    ripening with yellow flowers of mustard crop, which Hindus associate with Saraswati’s favorite
                    color. People dress in yellow saris or shirts or accessories, share yellow colored snacks and
                    sweets. Some add saffron to their rice then eat yellow cooked rice as a part of an elaborate feast.
                </p>
                <p>
                    Many families mark this day by sitting with babies and young children, encouraging their children to
                    write their first words with their fingers, some just study or create music together. The day before
                    Vasant Panchami, Saraswati’s temples are filled with food so that she can join the celebrants in the
                    traditional feasting the following morning. In temples and educational institutions, statues of
                    Saraswati are dressed in yellow and worshiped. Many educational institutions arrange special prayers
                    or pujas in the morning to seek blessing of the goddess. Poetic and musical gatherings are held in
                    some communities in reverence for Saraswati.
                </p>
                <p>
                    In Nepal, Bihar and eastern states of India such as West Bengal, Odisha and Assam, people visit her
                    temples and worship her (Saraswati Puja). Most of the schools arrange special Saraswati puja for
                    their students in their premises. In Bangladesh, all major educational institutes and universities
                    observe it with holiday and a special puja.
                </p>
                <p>
                    In southern states such as Telangana, Andhra Pradesh, the same day is called Sri Panchami where
                    “Sri” refers to her as another aspect of the one goddess Devi.
                </p>
